#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	int		i;
	char	*k;

	k = b;
	i = 0;
	while (k[i] && len)
	{
		k[i] = c;
		i++;
		len--;
	}
	return (k);
}
